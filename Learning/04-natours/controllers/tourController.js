const Tour = require('./../models/tourModel');
const ApiFeatures = require('./../utils/apiFeatures');

exports.aliasTopTours = async (req, res, next) => {
  req.query.limit = '5';
  req.query.sort = '-ratingsAverage,price';
  next();
};

exports.getALlTours = async (req, res) => {
  try {
    console.log(req.query);
    // EXECUTING QUERY
    const features = new ApiFeatures(Tour.find(), req.query)
      .filter()
      .sort()
      .limitFields()
      .paginate();
    const allTours = await features.query;

    // SENDING RESPONSE
    res.status(200).json({
      status: 'success',
      result: allTours.length,
      data: {
        allTours,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

exports.getTour = async (req, res) => {
  try {
    const id = req.params.id; // This is the way to get the parameters from the url and using the '?' this sign we can also declare optional url parameters // and multiplying it with 1 automatically converts the String to int if the value of String is 1
    const tour = await Tour.findById(id);
    if (tour) {
      res.status(200).send({
        status: 'success',
        data: {
          tour,
        },
      });
    } else {
      res.status(400).json({
        status: 'fail',
        message: 'Tour Not Found',
      });
    }
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

